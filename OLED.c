{  
	u8 x,y;  
	for(x=x1;x<=x2;x++)
	{
		for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot);
	}													    
	//OLED_Refresh_Gram();//更新显示
}

/*
 * 函数名：OLED_ShowChar
 * 描述  ：在指定位置显示一个字符,包括部分字符
 * 输入  ：x:0~127
           y:0~31
		   chr：要显示的字符
		   size:选择字体 32/16/12
		   mode:0,反白显示;1,正常显示 	
 * 输出  : 无
 * 调用  ：外部调用
*/
void OLED_ShowChar(u8 x,u8 y,u8 chr,u8 size,u8 mode)
{      			    
	u8 temp,t,t1;
	u8 y0=y;
	chr=chr-' ';//得到偏移后的值
	if(size == 32)
	{    
	    for(t=0;t<64;t++)
	    {   
			temp=oled_asc3_3216[chr][t];  //调用3216字体
	        for(t1=0;t1<8;t1++)
			{
				if(temp&0x80)OLED_DrawPoint(x,y,mode);
				else OLED_DrawPoint(x,y,!mode);
				temp<<=1;
				y++;
				if((y-y0)==size)
				{
					y=y0;
					x++;
					break;
				}
			}  	 
	    }	
	}
	else if(size == 12)
	{	   //单点模式			   
	    for(t=0;t<16;t++)
	    {   
			temp=oled_asc2_1206[chr][t];  //调用1206字体
			for(t1=0;t1<8;t1++)
			{
				if(temp&0x80)OLED_DrawPoint(x,y,mode);
				else OLED_DrawPoint(x,y,!mode);
				temp<<=1;
				y++;
				if((y-y0)==size)
				{
					y=y0;
					x++;
					break;
				}
			}  	 
	    }
	}
    else
    {
	    for(t=0;t<16;t++)
	    {   
		temp=oled_asc2_1608[chr][t];      //调用1608字体 ,每次Load一个byte，含自上到下，8个point,第char 行，第t列                         
	        for(t1=0;t1<8;t1++)
		{
			if(temp&0x80)OLED_DrawPoint(x,y,mode);
			else OLED_DrawPoint(x,y,!mode);
			temp<<=1;
			y++;
			if((y-y0)==size)
			{
				y=y0;
				x++;
				break;
			}
		}  	 
	    }
    }
    
}

/*
 * 函数名：OLED_ShowSymbol
 * 描述  ：在指定位置显示一个符号,包括部分符号
 * 输入  ：x:0~127
           y:0~31
		   sym：要显示的符号
		   mode:0,反白显示;1,正常显示 	
 * 输出  : 无
 * 调用  ：外部调用
*/
void OLED_ShowSymbol(u8 x,u8 y,u8 sym,u8 mode)
{
        u8 temp,t,t1;
	u8 y0=y;
	u8 x0=x;
	const unsigned char *cur_sym ;
	  //B0 B1 B2  B3  B4  B5  B6  B7
	  //B8 B9 B10 B11 B12 B13 B14 B15 
		   if(sym ==1)	//bt
		   {cur_sym = oled_bt_symbol ;}
		   else if(sym == 2)   //run
		   {cur_sym = oled_run_symbol ;}
		   else
		   {cur_sym = oled_stop_symbol;}

		  //先画第一行（每个byte高位到低位代表自上而下的8个点，第一行8个byte）
	       for(t=0;t<8;t++)
		    {   
			 temp=cur_sym[t];		 //调用1608字体 ,每次Load一个byte，含自上到下，8个point	                          
		        for(t1=0;t1<8;t1++)
				{
					if(temp&0x80)OLED_DrawPoint(x,y,mode);
					else OLED_DrawPoint(x,y,!mode);
					temp<<=1;
					y++;
					if((y-y0)==8)
					{
						y=y0;
						x++;
						break;
					}
				}  	 
		    }

			//将座标挪向第二行（8bit）
		    x = x0;
			y0 = y0+8;
			y = y0;
			//再画第二行
		   for(t=8;t<16;t++)
		    {   
			 temp=cur_sym[t];		 //调用1608字体 ,每次Load一个byte，含自上到下，8个point	                          
		        for(t1=0;t1<8;t1++)
				{
					if(temp&0x80)OLED_DrawPoint(x,y,mode);
					else OLED_DrawPoint(x,y,!mode);
					temp<<=1;
					y++;
					if((y-y0)==8)
					{
						y=y0;
						x++;
						break;
					}
				}  	 
		    }
		
}

/*
 * 函数名：oled_pow
 * 描述  ：m^n函数
 * 输入  ： 	
 * 输出  : 无
 * 调用  ：外部调用
*/
u32 oled_pow(u8 m,u8 n)
{
       
	u32 result=1;	 
	while(n--)result*=m;    
	return result;
}	

/*
 * 函数名：OLED_ShowNum
 * 描述  ：显示2个数字
 * 输入  ：x,y :起点坐标	
		   num:数值(0~4294967295);
		   len :数字的位数
		   size:字体大小 	
 * 输出  : 无
 * 调用  ：外部调用
*/	 		  
void OLED_ShowNum(u8 x,u8 y,int num,u8 len,u8 size)
{         	
	u8 t,temp;
	u8 enshow=0;						   
	for(t=0;t<len;t++)
	{
		temp=(num/oled_pow(10,len-t-1))%10;
		if(enshow==0&&t<(len-1))
		{
			if(temp==0)
			{
				OLED_ShowChar(x+(size/2)*t,y,' ',size,1);
				continue;
			}else enshow=1; 
		 	 
		}
	 	OLED_ShowChar(x+(size/2)*t,y,temp+'0',size,1); 
                
	}
     
} 

/*
 * 函数名：OLED_ShowString
 * 描述  ：显示字符串
 * 输入  ：x,y :起点坐标	
		   num:数值(0~4294967295);
		   size:字体大小(用16字体) 	
		   *p:字符串起始地址
 * 输出  : 无
 * 调用  ：外部调用
*/
void OLED_ShowString(u8 x,u8 y,u8 size,const u8 *p)
{
#define MAX_CHAR_POSX 122
#define MAX_CHAR_POSY 52          
    while(*p!='\0')
    {       
        if(x>MAX_CHAR_POSX){x=0;y+=16;}
        if(y>MAX_CHAR_POSY){y=x=0;OLED_Clear();}
        OLED_ShowChar(x,y,*p,size,1);
        if(size == 12)
          x+=8;
        else if(size == 16)
          x+=8;
        else
          x+=size/2;
        p++;
    }  
}


