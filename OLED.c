void write_oled_command(u8 ucCmd)
{
       u8 i;
       P4OUT &= ~BIT0;  // Set OLED_CS# LOW,enable   
       P4OUT &= ~BIT2;  // OLED_D/C# LOW, command
       for (i=0; i<8; i++)
        {
         P4OUT &= ~BIT3;
         if(ucCmd&0x80)
           P4OUT |= BIT4;
         else
          P4OUT &= ~BIT4;
          ucCmd = ucCmd << 1;
         P4OUT |= BIT3; 
         usDelay(2);
         }
       P4OUT &= ~BIT3;
       usDelay(2);
       P4OUT |= BIT2;
       P4OUT |= BIT0;       
}

/******************************************************************************
* Name: write_oled_data(unsigned char ucData)
* Function: write ucdata to oled
******************************************************************************/
void write_oled_data(unsigned char ucData)
{  
       u8 i;
       P4OUT &= ~BIT0;  // Set OLED_CS# LOW,enable   
       P4OUT |= BIT2;   // OLED_D/C# HIGH, data      
       for (i=0; i<8; i++)
        {
         P4OUT &= ~BIT3;
         if(ucData&0x80)
          P4OUT |= BIT4;
         else
          P4OUT &= ~BIT4;
         ucData = ucData << 1;
         P4OUT |= BIT3;
         usDelay(2);
        }
       P4OUT &= ~BIT3;
       usDelay(2);
       P4OUT |= BIT2;
       P4OUT |= BIT0;
}
