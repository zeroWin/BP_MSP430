void OLED_ShowWaitSymbol(u8 x,u8 y,u8 mode)
{
    u8 temp,t,t1;
    u8 y0=y;
    for(t=0;t<16;t++)
    {
      temp = oled_testing_point[t]; //调用16X8字体
      for(t1=0;t1<8;t1++)
      {
        if(temp&0x80)OLED_DrawPoint(x,y,mode);
        else OLED_DrawPoint(x,y,!mode);
        temp<<=1;
        y++;
        if((y-y0)==8)
        {
          y=y0;
          x++;
          break;
        }
      }  	
    }  
}

